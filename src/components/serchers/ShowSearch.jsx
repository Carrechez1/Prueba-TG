import React from "react";
import Pagination from "../Pagination";

const ShowSearch = ({ results, initialP, finalP, page, setPage, perPage }) => {
  /*****************cuando el arreglo este vacio sera falso******************** */
  if (results?.length == 0) {
    results = false;
  }
  /*****Se muestran los elementos filtrados por nombre y se aplica logica de paginacion****/
  console.log(results);
  return (
    <div>
      {results ? (
        <div>
          <article className="card__container2">
            {results?.slice(initialP, finalP).map((user) => (
              <div className="card" key={user.id}>
                <header className="card__header">
                  <h2 className="card__title">{user.name}</h2>
                </header>
                <ul className="card__list">
                  <div className="card__info">
                    <li className="card__item ">
                      <strong>Razon social: </strong>
                      {user.businessName}
                    </li>
                    <li className="card__item ">
                      <strong>Nit: </strong>
                      {user.nit}
                    </li>
                    <li className="card__item ">
                      <strong>Telefono: </strong>
                      {user.telf}
                    </li>
                    <li className="card__item ">
                      <strong>Codigo:</strong> {user.code}
                    </li>
                  </div>
                </ul>
              </div>
            ))}
          </article>
          <footer className="footer">
            <Pagination
              page={page}
              pagesLength={results && Math.ceil(results.length / perPage)}
              setPage={setPage}
            />
          </footer>
        </div>
      ) : (
        <div className="notFound">
          <p className="message">USUARIO NO ENCONTRADO</p>
          <img className="img" src="/no-encontrado.png" alt="" />
          <footer className="footer2"></footer>
        </div>
      )}
    </div>
  );
};

export default ShowSearch;
